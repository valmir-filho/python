# Exercício 8: Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
# Calcule e mostre o total do seu salário no referido mês.

x = float(input("Informe o quanto você ganha por hora: "))
y = float(input("Informe o número de horas trabalhadas no referido mês: "))
z = (x*y)
print("O total do seu salário no referido mês é:","%.2f"%z)
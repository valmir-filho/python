# Exercício 4: Faça um Programa que peça as 4 notas bimestrais e mostre a média.

x1 = float(input("Informe a nota do 1º bimestre: "))
x2 = float(input("Informe a nota do 2º bimestre: "))
x3 = float(input("Informe a nota do 3º bimestre: "))
x4 = float(input("Informe a nota do 4º bimestre: "))
print("A sua média é: ", (x1+x2+x3+x4)/4)
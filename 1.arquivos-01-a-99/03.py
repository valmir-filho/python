# Python possui tipagem dinâmica (reconhece se é número ou texto, por exemplo) e forte.

# Caracter de escape \
print("O melhor \"desenvolvedor\" do Brasil")

# Outra forma
print('O melhor "desenvolvedor" do Brasil')
print("O melhor 'desenvolvedor' do Brasil")

# Mostrar o caracter de escape
print(r"O melhor \desenvolvedor\ do Brasil")

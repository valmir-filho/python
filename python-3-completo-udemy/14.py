# Operadores relacionais (de comparação) em Python.

# Maior
maior = 2 > 1
print("Maior:", maior)

# Maior ou igual
maior_ou_igual = 2 >= 2
print("Maior ou igual:", maior_ou_igual)

# Menor
menor = 2 < 3
print("Menor:", menor)

# Menor ou igual
menor_ou_igual = 2 <= 2
print("Menor ou igual:", menor_ou_igual)

# Igual
igual = "a" == "a"
print("Igual:", igual)

# Diferente
diferente = "a" != "a"
print("Diferente:", diferente)

# Precedência dos operadores matemáticos.

# Parênteses
conta1 = 2 * (5 + 5)
print("Conta1:", conta1)

# Exponenciação (potência)
conta2 = 2 ** 5 + 5
print("Conta2:", conta2)

# Multiplicação, divisão, divisão inteira e resto da divisão
conta3 = 2 * 5 / 2 // 2 % 4  # Cálculos executados da esquerda para a direita
print("Conta3:", conta3)

# Adição e subtração
conta4 = 2 + 5 - 3 + 10
print("Conta4:", conta4)

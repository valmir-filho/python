# Método print.

print(12, 34)

# Por padrão, o método print gera um espaço entre os 2 argumentos. Para alterar isso, usamos: sep=""
print(12, 34, sep="")
print(12, 34, sep="-")

# Por padrão, o método print gera uma nova linha após a execução do comando. Podemos alterar isso usando: end=""
print(12, 34, sep="", end="")
print(56, 78, sep="")

# Operadores aritméticos.

# adição
adicao1 = 10 + 10
print("Adição:", adicao1)

adicao2 = 10.2 + 10
print("Adição:", adicao2)

# subtração
subtracao = 20 - 10
print("Subtração:", subtracao)

# multiplicação
multiplicacao = 10 * 10
print("Subtração:", multiplicacao)

# divisão
divisao = 10 / 2.2  # Sempre a resposta sai com ponto flutuante
print("Divisão:", divisao)

# divisão inteira
divisao_inteira = 10 // 2.2
print("Divisão inteira:", divisao_inteira)

# exponenciação
exponenciacao = 2 ** 10
print("Exponenciação:", exponenciacao)

# módulo
modulo = 25 % 5  # Resto da divisão
print("Módulo:", modulo)

print(10 % 8 == 0)  # Para saber se é múltiplo

print(10 % 2 == 0)  # Para saber se é par
